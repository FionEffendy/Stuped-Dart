void main() {
  int angka = 4;

  switch (angka) {
    case 1:
      print('One');
      break;
    case 2:
      print('Two');
      break;
    case 3:
      print('Three');
      break;
    default:
      print('Tidak Ada');
    // default digunakan ketika variabel di angka tidak ada di dalam case
  }
}
